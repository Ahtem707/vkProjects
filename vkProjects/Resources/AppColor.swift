import UIKit

enum AppColor {
    enum System {
        static let backColor = UIColor(0x0077FF)
        static let loader = UIColor(0x0E8AFD)
    }
    enum Text {
        static let white = UIColor(0xFFFFFF)
        static let primary = UIColor(0x000000)
        static let secondary = UIColor(0x6D6D6D)
        static let link = UIColor(0x0077FF)
    }
    
    enum View {
        static let error = UIColor(0xE20808)
        static let success = UIColor(0x3DA820)
    }
}
